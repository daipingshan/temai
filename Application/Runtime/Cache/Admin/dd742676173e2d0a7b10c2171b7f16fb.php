    function getSuffix(index, strPrice, url) {
        var openType = TM.utils.getOpenCommodityType(url);
        var numPrice = parseFloat(strPrice);
        var priceLevel = 'default';
        if (numPrice < 30) {
            priceLevel = 1;
        } else if (numPrice < 65) {
            priceLevel = 65;
        } else if (numPrice < 100) {
            priceLevel = 2;
        } else {
            priceLevel = 3;
        }
        return '&goodsIndex=' + index + '&priceLevel=' + priceLevel + '&openType=' + openType + '&appVersion=' + TM.ENV.version_str;
    }

